-- ============================================================
-- 凡人修仙传 - 境界配置表（24境界版）
-- 经典修仙体系：炼气→筑基→金丹→元婴→化神→炼虚→合体→大乘→渡劫
-- 数值递增规律：每个大境界约 2-3 倍，小境界约 1.3-1.5 倍
-- ============================================================

CREATE TABLE IF NOT EXISTS realms (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT UNIQUE NOT NULL,        -- 境界名称
  sort_order INTEGER NOT NULL,      -- 排序（从0开始）
  max_exp INTEGER NOT NULL,         -- 修为上限
  hp INTEGER NOT NULL,              -- 气血上限
  mp INTEGER NOT NULL,              -- 灵力上限
  atk INTEGER NOT NULL,             -- 攻击
  def INTEGER NOT NULL,             -- 防御
  speed REAL NOT NULL,              -- 修炼速度（每秒修为）
  lifespan INTEGER NOT NULL,        -- 寿元
  breakthrough_chance REAL DEFAULT 0.5, -- 基础突破概率
  created_at INTEGER
);

CREATE INDEX IF NOT EXISTS idx_realms_sort ON realms(sort_order);

